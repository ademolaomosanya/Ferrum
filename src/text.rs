//! Tiny built-in bitmap font and text wrapping shared by layout and paint.

pub const GLYPH_WIDTH: u32 = 5;
pub const GLYPH_HEIGHT: u32 = 7;
pub const SCALE: u32 = 2;
pub const ADVANCE: u32 = (GLYPH_WIDTH + 1) * SCALE;
pub const LINE_HEIGHT: u32 = (GLYPH_HEIGHT + 2) * SCALE;

pub fn wrap(text: &str, max_width: f32) -> Vec<String> {
    let columns = ((max_width.max(ADVANCE as f32) / ADVANCE as f32).floor() as usize).max(1);
    let mut lines = Vec::new();
    let mut current = String::new();

    for word in text.split_whitespace() {
        let separator = usize::from(!current.is_empty());
        if current.chars().count() + separator + word.chars().count() <= columns {
            if !current.is_empty() {
                current.push(' ');
            }
            current.push_str(word);
            continue;
        }

        if !current.is_empty() {
            lines.push(std::mem::take(&mut current));
        }
        let mut chunk = String::new();
        for character in word.chars() {
            chunk.push(character);
            if chunk.chars().count() == columns {
                lines.push(std::mem::take(&mut chunk));
            }
        }
        current = chunk;
    }

    if !current.is_empty() {
        lines.push(current);
    }
    if lines.is_empty() {
        lines.push(String::new());
    }
    lines
}

