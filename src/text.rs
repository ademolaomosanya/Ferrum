#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn wraps_at_word_boundaries() {
        assert_eq!(
            wrap("one two three", ADVANCE as f32 * 7.0),
            ["one two", "three"]
        );
    }

    #[test]
    fn splits_words_wider_than_the_line() {
        assert_eq!(wrap("browser", ADVANCE as f32 * 3.0), ["bro", "wse", "r"]);
    }
}
